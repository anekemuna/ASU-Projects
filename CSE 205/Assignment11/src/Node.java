public class Node
{
    // instance variables
    private int x;
    private int y;

    // Node(): constructor
    public Node(int x, int y)
    {
        this.x = x;
        this.y = y;
    }

    // getX() return the value of x
    public int getX()
    {
        return x;
    }

    // getY() return the value of y
    public  int getY()
    {
        return y;
    }
}
